package Pojo;

public class Location {

	private double lat;
	private double lng;
	
	//getter and setter for lat
	public double getLat() {
		return lat;
	}
	public void setLat(double lat) {
		this.lat = lat;
	}
	//getter and setter for longt
	public double getLng() {
		return lng;
	}
	public void setLng(double lng) {
		this.lng = lng;
	}
	
	
}
